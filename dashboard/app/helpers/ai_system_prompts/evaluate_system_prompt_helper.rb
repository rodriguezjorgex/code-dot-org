module AiSystemPrompts::EvaluateSystemPromptHelper
  def self.get_system_prompt(level, unit, evaluation_type)
    evaluation_criteria = get_evaluation_criteria(level)
    evaluation_summary = get_evaluation_criteria_summary(level)
    skill_evaluations = get_skill_evaluations(level)
    # TODO figure out how to handle skills id
    skills = level.skills
    structure_with_skills = <<~TEXT
      Review the student's work. Respond in correctly formatted JSON.
      evaluationCriteria should be a copy of #{evaluation_summary}.
      aiEvaluation should be your overall assessment of the student's work. Respond with "Great", "Ok", or "Needs revision".
      aiReasoning should be one sentence with your reasoning.
      skillEvaluations should be an array of objects based on #{skill_evaluations} with the following structure: {skillId: skillId, evaluationCriteria: evaluationCriteria, aiEvaluation: Respond with "Great", "Ok", "Needs revision" or "not attempted", aiReasoning: "reasoning for specific evaluation"}. generated by iterating through the skills.
    TEXT
    structure_without_skills = <<~TEXT
      Review the student's work. Respond in correctly formatted JSON.
      evaluationCriteria should be a copy of #{evaluation_criteria}.
      aiEvaluation should be your overall assessment of the student's work. Respond "Needs revision" only if the student's answer is completely unrelated (e.g. “idk”, “I don't know”, "who cares", or "na"), off-topic, or the student's answer incorrectly answers the questions asked in the level instructions. Respond with "Great" if the student's answer accurately answers all parts of the questions asked or directions given in the level instructions. Respond "Ok" if the student's work partially meets the requirements of the level instructions.
      aiReasoning should be one sentence with your reasoning.
    TEXT
    single_student_evaluation_structure = skills.empty? ? structure_without_skills : structure_with_skills
    evaluation_structure = evaluation_type == SharedConstants::AI_EVALUATION_TYPES[:SINGLE_STUDENT] ? single_student_evaluation_structure : section_summary_evaluation_structure
    prompt = AiSystemPrompts::SystemPromptHelper.get_basic_system_prompt(level, unit)
    prompt << evaluation_structure
  end

  def self.get_evaluation_criteria_summary(level)
    skills = level.skills
    summary = "Evaluated for completion based on a level instructions."
    if skills.empty? && level.upper_grades_programming_level?
      summary << "/n Evaluated for general code quality."
    elsif skills.any?
      summary << "/n Evaluated for #{skills.length} programming concept skills."
    end
    summary
  end

  def self.get_skill_evaluations(level)
    skills = level.skills
    skill_evaluations = []
    if skills.any?
      skills.each do |skill|
        skill_evaluations << {
          skillId: skill.id,
          evaluationCriteria: skill.evaluation_criteria
        }
      end
    end
    skill_evaluations
  end

  def self.get_evaluation_criteria(level)
    completion_criteria = "Did the student complete all of the requirements in the instructions?"
    evaluation_criteria = [completion_criteria]
    skills = level.skills
    if skills.any?
      skills.each do |skill|
        evaluation_criteria << skill.evaluation_criteria
      end
    elsif level.upper_grades_programming_level?
      code_quality_criteria = "Does the code run without errors? Does the code follow best practices?"
      evaluation_criteria << code_quality_criteria
    end
    evaluation_criteria
  end
end
